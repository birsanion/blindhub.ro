            <h1>De ce noi?</h1><br />
            
            <p>Pentru că, prin misiunea sa statutară, organizaţia noastră este unicul furnizor din România de programe socio-umanitare, cultural-educaţionale şi sportive destinate deficienţilor de vedere.</p>
            <p>De asemenea, pentru că experienţa de 65 de ani a ANR, de promovare şi luptă continuă pentru drepturile celor cca 100.000 de deficienţi de vedere din România ne recomandă ca fiind organizaţia abilitată pentru a ajuta în mod eficient oamenii vulnerabili din comunitatea noastră.</p>
            <p>Pentru că ne-au acordat încrederea prin adeziunea lor peste 38.000 de persoane încadrate în grad de handicap vizual grav, mediu şi accentuat, pentru care asociaţia oferă, prin cele 33 de filiale de pe întreg teritoriul ţării, servicii sociale, consultanţă juridică, fiscală şi în relaţia cu instituţiile statului. Membrii organizaţiei participă activ la seminarele socio - profesionale, la concursurile culturale şi la competiţiile sportive organizate de ANR. Totodată, toţi membrii au acces la fondul de carte accesibilizat în braille sau audio şi la publicaţiile realizate în singura tipografie braille din România şi în studiourile de înregistrări.</p>
            <p>Totodată, pentru că ANR este cel mai mare angajator al persoanelor cu dizabilități vizuale, o categorie de altfel care se confruntă cu bariere reale pe piața forței de muncă. </p>
            
