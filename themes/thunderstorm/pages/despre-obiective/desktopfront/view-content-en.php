                    <p>Technology for the professional development of visually impaired people.</p>
                    <p class="mt-4"><strong>The BlindHUB international projects</strong> - ConnectBlind.com, BlindHub.org, icanBlind.com, AveuglesHub.com, CiegoHub.com are implemented by the SNAC Romania Association and Tarabostes software.</p>
                    <p>The BlindHUB project is implemented in Romania by the Association of the Blind in Romania in partnership with the University of Bucharest and is funded by the Orange Foundation through the World through Color and Sound Program - the largest fund dedicated to people with visual impairments in Romania.</p>
                    <p>Aim of the project: Facilitating the access of blind and visually impaired people on the labor market and in the university educational environment.</p>


                    <h5 class="mt-5 subtitlu">Objectives:</h5>
                    <ul class="lista">
                        <li>Development and adaptation of the BlindHUB mobile application for the specifics of each country, an application that provides information, guidance and counseling for career and digital mediation on the labor market between blind and visually impaired people and employers</li>
                        <li>Training the skills of presenting oneself and holding a job interview using the BlindHUB application for a number of blind and visually impaired people by organizing 10 seminars nationwide with the participation of employers</li>
                        <li>Establishment and endowment with computer technology and accessibility of BlindHUB centers - professional development communities for the blind and visually impaired</li>
                    </ul>
