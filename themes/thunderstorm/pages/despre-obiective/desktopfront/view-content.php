                <h1 class="titlu ">Obiective</h1>
                <article>
                    <p><strong>Proiectul BlindHUB</strong> este implementat de catre <strong>Asociatia Nevazatorilor din Romania</strong> in parteneriat cu <strong>Universitatea din Bucuresti</strong> si este finantat de <strong>Fundatia Orange</strong> prin <strong>Programul Lumea prin Culoare si Sunet</strong> - cel mai mare fond dedicat persoanelor cu deficiente de vedere / auz din România.</p>
                    <p><strong>Scopul proiectului</strong>: Facilitarea accesului persoanelor nevazatoare si cu deficient de vedere pe piata muncii si in mediul educational universitar.</p>
                    <h5 class="mt-5 subtitlu">Obiective:</h5>
                    <ul class="lista">
                        <li>Dezvoltarea unei aplicatii informatice (BlindHUB) care sa asigure informare, orientare si consiliere pentru cariera si mediere digitala pe piata muncii intre persoanele nevazatoare si cu deficiente de vedere si angajatori</li>
                        <li>Formarea abilitatilor de prezentare a propriei persoane si de sustinere a unui interviu de angajare folosid aplicatia BlindHUB pentru un numar de cel putin 200 de persoane nevazatoare si cu deficient de vedere prin organizarea a 10 seminarii la nivel national cu participarea angajatorilor</li>
                        <li>Infiintarea si dotarea cu tehnica informatica si de accesibilizare a 2 centre BlindHUB – comunitati de dezvoltare profesionala pentru persoanele nevazatoare si cu deficient de vedere</li>
                    </ul>
                    <br />
                    <p>Proiectul se deruleaza in perioada <strong>octombrie 2020 - octombrie 2022</strong>, finantarea  totala este <strong>532.000</strong> lei, contributia <strong>Fundatiei Orange</strong> fiind de <strong>482.000</strong> lei.</p>
