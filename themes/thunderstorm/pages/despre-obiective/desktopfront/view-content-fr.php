                    <p>L&#39;objectif principal est d&#39;augmenter la qualité de vie des personnes ayant des besoins spéciaux.</p>
                    <p class="mt-4"><strong>Les projets internationaux BlindHUB</strong> - connectblind.com, blindhub.org, icanblind.com, AveuglesHub.com, CiegoHub.com sont mis en œuvre par l&#39;association SNAC Roumanie et le logiciel Tarabostes.</p>
                    <p>Le projet BlindHUB est mis en œuvre en Roumanie par l&#39;Association roumaine des aveugles en partenariat avec l&#39;Université de Bucarest et est financé par la Fondation Orange à travers le programme World through Color and Sound - le plus grand fonds dédié aux personnes ayant une déficience visuelle en Roumanie.</p>
                    <p>Objectif du projet: Faciliter l&#39;accès des personnes aveugles et malvoyantes au marché du travail et au milieu éducatif universitaire.</p>

