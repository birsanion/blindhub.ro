                        <li>Formation aux compétences de présentation et de tenue d&#39;un entretien d&#39;embauche à l&#39;aide del&#39;application BlindHUB pour un certain nombre de personnes aveugles et malvoyantes en organisant 10 séminaires à l&#39;échelle nationale avec la participation d&#39;employeurs</li>
