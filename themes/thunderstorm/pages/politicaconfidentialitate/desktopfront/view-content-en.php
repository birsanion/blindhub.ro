                <p class="my-3">
                    <strong>
                        Principles of personal data processing
                    </strong>
                </p>
                <p>
                    Principles related to the processing of personal data require that personal data be:
                </p>
                <p>
                    - processed lawfully, fairly and transparently to the data subject ("legality, fairness and transparency");<br>
                    - collected for specified, explicit and legitimate purposes and not subsequently processed in a manner incompatible with those purposes; further processing for archiving purposes in the public interest, for scientific or historical research or for statistical purposes is not considered incompatible with the original purposes, in accordance with Article 89 (1) of the GDPR ("purpose-related limitations");<br>
                    - appropriate, relevant and limited to what is necessary in relation to the purposes for which they are processed ("data minimization");<br>
                    - accurate and, if necessary, updated; all necessary measures must be taken to ensure that personal data which are inaccurate, having regard to the purposes for which they are processed, are deleted or rectified without delay ('accuracy');<br>
                    - kept in a form which permits identification of data subjects for a period not exceeding the period necessary for the purposes for which the data are processed; personal data may be stored for longer periods to the extent that they are processed exclusively for archiving purposes in the public interest, for scientific or historical research or for statistical purposes, in accordance with Article 89 (1) of GDPR, subject to the implementation of appropriate technical and organizational measures provided for in the GDPR Regulation in order to guarantee the rights and freedoms of the data subject ("storage restrictions");<br>
                    - processed in a way that ensures adequate security of personal data, including protection against unauthorized or unlawful processing and against accidental loss, destruction or damage, by taking appropriate technical or organizational measures ("integrity and confidentiality").
                </p>
