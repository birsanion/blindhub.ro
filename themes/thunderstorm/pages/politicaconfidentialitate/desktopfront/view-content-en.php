                <p>
                    As of 25.05.2018, Regulation 2016/679 on the protection of individuals with regard to the processing of personal data and on the free movement of such data, also known as the GDPR (General Data Protection Regulation), becomes applicable.
                </p>
                <p>
                    What is personal data?
                </p>
                <p>
                    Personal data is any information that relates to an identified or identifiable natural person. The various pieces of information that may lead to the identification of a particular person also constitute personal data. Personal data that has been anonymized, encrypted or pseudonymized but can be used to re-identify a person remains personal data and is covered by the GDPR (RGPD). Personal data that has been made anonymous in such a way that the individual is no longer or is no longer identifiable is no longer considered personal data. For data to be truly anonymized, anonymization must be irreversible.
                </p>
                <p>
                    RGPD protects personal data regardless of the technology used to process the data - it is "technology neutral" and applies to both automatic and manual processing, provided that the data is organized according to predefined criteria (e.g. , in alphabetical order). It also doesn't matter how the data is stored - in an ICT system, by video surveillance or on paper; In all these cases, personal data are subject to the protection requirements set out in the RGPD.
                </p>
