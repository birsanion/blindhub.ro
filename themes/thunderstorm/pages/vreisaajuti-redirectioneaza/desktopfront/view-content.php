            <h1>Ajuţi fără să te coste nimic!</h1><br /><br />

            <p>Redirecţionează 3,5% din impozitul pe veniturile salariale şi asimilate
                salariilor către Asociaţia Nevăzătorilor din România (ANR) şi
                arată că îţi pasă de oamenii cu probleme de vedere!</p>

            <br /><h1>De ce să faci asta?</h1><br />
            <p>Pentru că doar astfel susţii în mod concret programe şi proiecte
                sociale, acţiuni umanitare, educative, culturale şi sportive destinate persoanelor cu deficienţă vizuală.</p>

            <p>Pentru că doar astfel ajuţi un nevăzător:</p>

            <ol style="list-style: disc; list-style-position: inside;">
                <li>să fie informat despre drepturile pe care le are;</li>
                <li>să fie respectat în relaţia cu instituţiile statului şi să poată accesa orice serviciu public;</li>
                <li>să poată merge pe stradă având un baston şi marcaje tactile sau sonore de îndrumare;</li>
                <li>să poată munci având un calculator adaptat cu un cititor de ecran;</li>
                <li>să poată citi ascultând o carte;</li>
                <li>să aibă acces, la fel ca tine, la toate lucrurile obişnuite
                    necesare vieţii de zi cu zi: un ceas vorbitor, un termometru cu sinteză vocală etc.</li>
            </ol>

            <br /><h1>De ce noi?</h1><br />

            <p>ANR apără şi promovează de peste 65 de ani drepturile celor cca 100.000 de deficienţi de vedere din România. Prin misiunea sa statutară, organizaţia desfăşoară ample programe socio-umanitare, culturale şi sportive pentru îmbunătăţirea vieţii de zi cu zi şi integrarea persoanelor cu diferite grade de deficienţă vizuală, programe pe care nu le oferă nici o instituţie de stat sau privată.</p>
            <p>Ne-au acordat încrederea, prin adeziunea lor, peste 38.000 de persoane încadrate în grad de handicap vizual grav, mediu şi accentuat, pentru care asociaţia oferă, prin cele 33 de filiale de pe întreg teritoriul ţării, servicii sociale, consultanţă juridică, fiscală şi în relaţia cu instituţiile statului. Membrii organizaţiei participă activ la seminarele socio - profesionale, la concursurile culturale şi la competiţiile sportive organizate de ANR. Totodată, toţi membrii au acces la fondul de carte accesibilizat în braille sau audio şi la publicaţiile realizate în singura tipografie braille din România şi în studiourile de înregistrări.</p>
            <p>ANR este cel mai mare angajator al persoanelor cu dizabilități vizuale, o categorie de altfel care se confruntă cu bariere reale pe piața forței de muncă.</p>

            <br /><h1>Cum?</h1><br />

            <p>1. Cerere adresată angajatorului pentru veniturile anului în curs</p>
            <p>Poţi începe redirecţionarea a 3,5% din impozitul pe veniturile salariale şi asimilate salariilor datorate în acest an chiar acum, printr-o simplă cerere adresată angajatorului (un model de cerere poţi descărca aici).</p>
            <br /><p>2. Declarația 230 pentru veniturile anului trecut</p>
